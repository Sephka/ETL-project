CREATE TABLE bitstamp (
     currency VARCHAR(30),
    date VARCHAR(30),
    open NUMERIC(12, 2),
    high NUMERIC(12, 2),
    low NUMERIC(12, 2),
    close NUMERIC(12, 2)
);

